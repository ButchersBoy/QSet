using System;
using System.Drawing;

namespace Mulholland.WinForms.Controls
{
	/// <summary>
	/// COntains colour settings for formatting XML.
	/// </summary>
	public struct XmlColours
	{		
		/// <summary>
		/// Special character colour.
		/// </summary>
		public Color SpecialCharacter;

		/// <summary>
		/// Element name colour.
		/// </summary>
		public Color ElementName;

		/// <summary>
		/// Element value colour.
		/// </summary>
		public Color ElementValue;

		/// <summary>
		/// Attribute name colour.
		/// </summary>
		public Color AttributeName;

		/// <summary>
		/// Attribute value colour.
		/// </summary>
		public Color AttributeValue;

	}
}
